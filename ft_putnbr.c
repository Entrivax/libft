#include <unistd.h>

void	ft_putnbr(int n)
{
	char	a;
	char	c;

	a = n % 10;
	c = '0';
	if (n < 0)
		write(1, "-", 1);
	if ((n /= 10) != 0)
		ft_putnbr(n < 0 ? -n : n);
	c += a < 0 ? -a : a;
	write(1, &c, 1);
}
