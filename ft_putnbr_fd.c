#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	a;
	char	c;

	a = n % 10;
	c = '0';
	if (n < 0)
		write(fd, "-", 1);
	if ((n /= 10) != 0)
		ft_putnbr_fd(n < 0 ? -n : n, fd);
	c += a < 0 ? -a : a;
	write(fd, &c, 1);
}
